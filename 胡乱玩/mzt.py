headers = {'User-Agent': random.choice(UserAgent_List),
           'Accept': "text/html,application/xhtml+xml,application/xml;q=0.9,image/webp,*/*;q=0.8",
           'Accept-Encoding': 'gzip',
           }

page_url = 'http://www.meizitu.com/a/more_1.html'

def get_page_atlases(page_url):
    page_atlas_url_list= []
    onepage = requests.get(page_url,  headers=headers).text
    soup = BeautifulSoup(onepage, 'lxml')
    pageatlas = soup.select('li.wp-item > div > div > a')
    for item in pageatlas:
        page_atlas_url_list.append(item.get('href'))
    return page_atlas_url_list
    

def get_imgs_info(url):
    atlas = requests.get(url, headers=headers).text
    soup = BeautifulSoup(atlas, 'lxml')
    images = soup.select('p > img')
    return images
    
def downloadimgs(list):
    for item in list:
        url = item.get('src')
        img_html = requests.get(url, headers=headers)
        title = item.get('alt')
        with open(title + '.jpg', 'wb') as f:
            f.write(img_html.content)
            

            
page_atlas_url_list = get_page_atlases(page_url)

